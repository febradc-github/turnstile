#!/usr/bin/env node
'use strict';

const fs = require('node:fs');
const path = require('node:path');
const { spawnSync } = require('node:child_process');

function platformName(platform) {
  if (platform === 'win32') return 'windows';
  if (platform === 'darwin') return 'macos';
  if (platform === 'linux') return 'linux';
  return null;
}

function windowsInstallPath(deps) {
  const candidates = [];
  if (deps.env.LOCALAPPDATA) {
    candidates.push(path.win32.join(deps.env.LOCALAPPDATA, 'Programs', 'Obsidian', 'Obsidian.exe'));
  }
  candidates.push('C:\\Program Files\\Obsidian\\Obsidian.exe');
  return candidates.find((p) => deps.exists(p)) || null;
}

function detectInstall(deps) {
  const name = platformName(deps.platform);
  if (name === 'windows') {
    const installPath = windowsInstallPath(deps);
    return { alreadyInstalled: installPath !== null, installPath };
  }
  if (name === 'macos') {
    const appPath = '/Applications/Obsidian.app';
    if (deps.exists(appPath)) return { alreadyInstalled: true, installPath: appPath };
    return { alreadyInstalled: false, installPath: null };
  }
  if (name === 'linux') {
    if (deps.run('snap', ['list', 'obsidian']).status === 0) {
      return { alreadyInstalled: true, installPath: 'snap:obsidian' };
    }
    const flatpak = deps.run('flatpak', ['list', '--app']);
    if (flatpak.status === 0 && flatpak.stdout.includes('md.obsidian.Obsidian')) {
      return { alreadyInstalled: true, installPath: 'flatpak:md.obsidian.Obsidian' };
    }
    return { alreadyInstalled: false, installPath: null };
  }
  return { alreadyInstalled: false, installPath: null };
}

const PACKAGE_MANAGERS = {
  windows: [
    { name: 'winget', check: ['winget', ['--version']], install: ['winget', ['install', 'Obsidian.Obsidian']] },
  ],
  macos: [
    { name: 'brew', check: ['brew', ['--version']], install: ['brew', ['install', '--cask', 'obsidian']] },
  ],
  linux: [
    { name: 'snap', check: ['snap', ['version']], install: ['snap', ['install', 'obsidian', '--classic']] },
    { name: 'flatpak', check: ['flatpak', ['--version']], install: ['flatpak', ['install', '-y', 'flathub', 'md.obsidian.Obsidian']] },
  ],
};

function resolvePackageManager(deps) {
  const candidates = PACKAGE_MANAGERS[platformName(deps.platform)] || [];
  for (const pm of candidates) {
    if (deps.run(pm.check[0], pm.check[1]).status === 0) return pm;
  }
  return null;
}

function commandString([cmd, args]) {
  return [cmd, ...args].join(' ');
}

function detect(deps) {
  const platform = platformName(deps.platform);
  if (platform === null) {
    return { platform: null, alreadyInstalled: false, installPath: null, packageManager: null, installCommand: null };
  }
  const { alreadyInstalled, installPath } = detectInstall(deps);
  const pm = resolvePackageManager(deps);
  return {
    platform,
    alreadyInstalled,
    installPath,
    packageManager: pm ? pm.name : null,
    installCommand: pm ? commandString(pm.install) : null,
  };
}

function install(deps) {
  const pm = resolvePackageManager(deps);
  if (!pm) {
    return {
      success: false,
      message: 'No supported package manager found; install manually from https://obsidian.md/download',
    };
  }
  const result = deps.run(pm.install[0], pm.install[1]);
  const output = [result.stdout, result.stderr].filter(Boolean).join('\n').trim();
  if (result.status === 0) {
    return { success: true, message: output || 'Installed.' };
  }
  return {
    success: false,
    message: `\`${commandString(pm.install)}\` exited with status ${result.status}:\n${output}`,
  };
}

// Captured verbatim from a vault generated by Obsidian on 2026-07-03 —
